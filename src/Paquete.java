public class Paquete {

    private double identificador;
    private String tipo;
    private double peso;
    private double valorDeclarado;

    public Paquete(double identificador, String tipo, double peso, double valorDeclarado) {
        this.identificador = identificador;
        this.tipo = tipo;
        this.peso = peso;
        this.valorDeclarado = valorDeclarado;
    }
}
